public class Main
{
    public static void main(String[] args)
    {
        for(int c = 0; c <= 30; c++)
        {
            System.out.print("" + c + " ");
        }
    }
}

class Decrementing
{
    public static void main(String[] args)
    {
        for(int n = 30; n >= 0; n--)
        {
            System.out.print("" + n + " ");
        }
    }
}

class UpByThree
{
    public static void main(String[] args)
    {
        for(int t = 0; t <= 18; t += 3)
        {
            System.out.print("" + t + " ");
        }
    }
}